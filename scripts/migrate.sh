  sleep 3
  log_success "Conteneur WordPress pr√™t"
}

ensure_composer() {
  log_info "V√©rification de Composer dans le conteneur..."
  
  local compose_cmd=$(pick_compose)
  
  # V√©rifier si Composer est d√©j√† install√©
  if $compose_cmd exec -T wordpress bash -c "command -v composer >/dev/null 2>&1"; then
    log_success "Composer d√©j√† install√©"
    return
  fi
  
  log_info "Installation de Composer dans le conteneur..."
