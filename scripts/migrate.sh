install_npm_dependencies() {
  log_info "Installation des d√©pendances npm dans le th√®me personnalis√©..."
  
  # Charger les variables d'environnement
  set -a
  source .env
  set +a
  
  # Fonction pour g√©n√©rer le slug
  slugify() {
    local input="$1"
    local slug
    slug=$(echo "$input" | iconv -f UTF-8 -t ASCII//TRANSLIT 2>/dev/null | sed -E 's/[^a-zA-Z0-9]+/-/g' | sed -E 's/^-+|-+$//g' | tr '[:upper:]' '[:lower:]')
    if [ -z "$slug" ]; then
      slug=$(echo "$input" | sed -E 's/[^a-zA-Z0-9]+/-/g' | sed -E 's/^-+|-+$//g' | tr '[:upper:]' '[:lower:]')
    fi
    printf '%s\n' "$slug"
  }
  
  local custom_theme_slug="${CUSTOM_THEME_SLUG:-$(slugify "$CUSTOM_THEME_NAME")}"
  local custom_theme_container_path="/var/www/html/wp-content/themes/$custom_theme_slug"
  local custom_theme_host_path="wordpress/wp-content/themes/$custom_theme_slug"
  
  if [ ! -d "$custom_theme_host_path" ]; then
    log_warning "Th√®me personnalis√© non trouv√© : $custom_theme_host_path"
    log_info "Le th√®me sera cr√©√© lors de la prochaine ex√©cution de setup-wordpress.sh"
    return
  fi
  
  if [ ! -f "$custom_theme_host_path/package.json" ]; then
    log_warning "Pas de package.json dans le th√®me personnalis√©"
    return
  fi
  
  log_info "Ex√©cution de npm install dans le conteneur WordPress..."
  
  local compose_cmd=$(pick_compose)
  $compose_cmd exec -T wordpress bash -c "cd '$custom_theme_container_path' && npm install" || {
    log_error "√âchec de npm install dans le th√®me personnalis√©"
    exit 1
  }
  
  log_success "D√©pendances npm install√©es pour le th√®me personnalis√©"
}

